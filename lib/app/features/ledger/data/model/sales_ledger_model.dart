import 'package:easy_vat_v2/app/features/ledger/domain/entities/sales_ledger_entity.dart';

class SalesLedgerModel extends SalesLedgerEntity {
  SalesLedgerModel({
    super.ledgerIdpk,
    super.underIdfk,
    super.ledgerName,
    super.description,
    super.taxPercentage,
    super.mailingName,
    super.billingAddress,
    super.taxability,
    super.mobile,
    super.phone,
    super.isActive,
    super.openingBalance,
    super.openingBalanceType,
    super.currentBalance,
    super.currentBalanceType,
    super.contactPerson,
    super.creditDays,
    super.creditLimit,
    super.createdBy,
    super.createdDate,
    super.modifiedDate,
    super.isEdit,
    super.currentBalanceTran,
  });

  factory SalesLedgerModel.fromJson(Map<String, dynamic> json) =>
      SalesLedgerModel(
        ledgerIdpk: json["ledgerIDPK"],
        underIdfk: json["underIDFK"],
        ledgerName: json["ledgerName"],
        description: json["description"],
        taxPercentage: json["taxPercentage"],
        mailingName: json["mailingName"],
        billingAddress: json["billingAddress"],
        taxability: json["taxability"],
        mobile: json["mobile"],
        phone: json["phone"],
        isActive: json["isActive"],
        openingBalance: json["openingBalance"],
        openingBalanceType: json["openingBalanceType"],
        currentBalance: json["currentBalance"].toDouble(),
        currentBalanceType: json["currentBalanceType"],
        contactPerson: json["contactPerson"],
        creditDays: json["creditDays"],
        creditLimit: json["creditLimit"],
        createdBy: json["createdBy"],
        createdDate: DateTime.parse(json["createdDate"]),
        modifiedDate: DateTime.parse(json["modifiedDate"]),
        isEdit: json["isEdit"],
        currentBalanceTran: json["currentBalanceTran"].toDouble(),
      );

  Map<String, dynamic> toJson() => {
        "ledgerIDPK": ledgerIdpk,
        "underIDFK": underIdfk,
        "ledgerName": ledgerName,
        "description": description,
        "taxPercentage": taxPercentage,
        "mailingName": mailingName,
        "billingAddress": billingAddress,
        "taxability": taxability,
        "mobile": mobile,
        "phone": phone,
        "isActive": isActive,
        "openingBalance": openingBalance,
        "openingBalanceType": openingBalanceType,
        "currentBalance": currentBalance,
        "currentBalanceType": currentBalanceType,
        "contactPerson": contactPerson,
        "creditDays": creditDays,
        "creditLimit": creditLimit,
        "createdBy": createdBy,
        "isEdit": isEdit,
        "currentBalanceTran": currentBalanceTran,
      };
}
