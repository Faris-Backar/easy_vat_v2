import 'package:easy_vat_v2/app/features/ledger/domain/entities/cash_ledger_enitiy.dart';

class CashLedgerModel extends CashLedgerEntity {
  int countryIdfk;
  int countryStatIdfk;
  String taxRegistrationType;
  DateTime dateOfTaxRegistration;
  CashLedgerModel({
    required this.countryIdfk,
    required this.countryStatIdfk,
    required this.taxRegistrationType,
    required this.dateOfTaxRegistration,
    super.billingAddress,
    super.contactPerson,
    super.createdBy,
    super.createdDate,
    super.currentBalance,
    super.currentBalanceTran,
    super.currentBalanceType,
    super.description,
    super.isActive,
    super.isEdit,
    super.ledgerIdpk,
    super.ledgerName,
    super.mailingName,
    super.mobile,
    super.modifiedDate,
    super.openingBalance,
    super.openingBalanceType,
    super.phone,
    super.taxRegistrationNo,
    super.taxPercentage,
    super.rowguid,
    super.taxability,
    super.underIdfk,
  });

  factory CashLedgerModel.fromJson(Map<String, dynamic> json) =>
      CashLedgerModel(
        ledgerIdpk: json["ledgerIDPK"],
        underIdfk: json["underIDFK"],
        ledgerName: json["ledgerName"],
        description: json["description"],
        taxPercentage: json["taxPercentage"],
        mailingName: json["mailingName"],
        billingAddress: json["billingAddress"],
        countryIdfk: json["countryIDFK"],
        countryStatIdfk: json["countryStatIDFK"],
        taxability: json["taxability"],
        taxRegistrationType: json["taxRegistrationType"],
        dateOfTaxRegistration: DateTime.parse(json["dateOfTaxRegistration"]),
        taxRegistrationNo: json["taxRegistrationNo"],
        mobile: json["mobile"],
        phone: json["phone"],
        isActive: json["isActive"],
        openingBalance: json["openingBalance"],
        openingBalanceType: json["openingBalanceType"],
        currentBalance: json["currentBalance"].toDouble(),
        currentBalanceType: json["currentBalanceType"],
        contactPerson: json["contactPerson"],
        createdBy: json["createdBy"],
        createdDate: DateTime.parse(json["createdDate"]),
        rowguid: json["rowguid"],
        modifiedDate: DateTime.parse(json["modifiedDate"]),
        isEdit: json["isEdit"],
        currentBalanceTran: json["currentBalanceTran"].toDouble(),
      );

  Map<String, dynamic> toJson() => {
        "ledgerIDPK": ledgerIdpk,
        "underIDFK": underIdfk,
        "ledgerName": ledgerName,
        "description": description,
        "taxPercentage": taxPercentage,
        "mailingName": mailingName,
        "billingAddress": billingAddress,
        "countryIDFK": countryIdfk,
        "countryStatIDFK": countryStatIdfk,
        "taxability": taxability,
        "taxRegistrationType": taxRegistrationType,
        "dateOfTaxRegistration": dateOfTaxRegistration.toIso8601String(),
        "taxRegistrationNo": taxRegistrationNo,
        "mobile": mobile,
        "phone": phone,
        "isActive": isActive,
        "openingBalance": openingBalance,
        "openingBalanceType": openingBalanceType,
        "currentBalance": currentBalance,
        "currentBalanceType": currentBalanceType,
        "contactPerson": contactPerson,
        "createdBy": createdBy,
        "rowguid": rowguid,
        "isEdit": isEdit,
        "currentBalanceTran": currentBalanceTran,
      };
}
